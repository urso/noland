"use client";

import React from 'react';

interface ActionButtonProps {
  onClick: () => Promise<void>;
  icon: React.ReactNode;
  title: string;
  isLoading: boolean;
  disabled?: boolean;
  loadingAnimation?: string;
  hoverColor?: string;
}

export default function ActionButton({
  onClick,
  icon,
  title,
  isLoading,
  disabled = false,
  loadingAnimation = 'animate-spin',
  hoverColor = 'hover:text-gray-700'
}: ActionButtonProps) {
  return (
    <button
      className={`p-1 text-gray-500 ${hoverColor} disabled:opacity-50`}
      onClick={onClick}
      disabled={isLoading || disabled}
      title={title}
      aria-label={title}
    >
      <div className={isLoading ? loadingAnimation : ''}>
        {icon}
      </div>
    </button>
  );
}

